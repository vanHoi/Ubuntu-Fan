    <h1>De naam "Ubuntu"</h1>

    <h6>Ubuntu</h6>
    <p>Ubuntu is een Zuid-Afrikaanse idee dat de nadruk legt op de communicatie en relatie met elkaar. Het woord komt
        uit de Zulu stammen. Ubuntu wordt gezien als een traditioneel Afrikaans idee en als een van de basisprincipes
        voor de nieuwe republiek van Zuid-Afrika.

        <br/><br/>Een ruwe vertaling van het Ubuntu-principe is: "medemenselijkheid". Een andere vertaling zou kunnen
        zijn: "de overtuiging dat delen met elkaar een universeel verbond schept dat de gehele mensheid bindt".

        <br/><br/>"Een persoon met 'ubuntu' is open en staat klaar voor anderen, geeft erkenning, voelt zich niet
        bedreigd wanneer anderen beter zijn, want hij of zij bezit de zelfverzekerdheid die ontstaat door de wetenschap
        dat hij of zij deel uitmaakt van een groter geheel, en voelt zich gekleineerd wanneer anderen vernederd of
        gekleineerd worden, wanneer anderen gemarteld of onderdrukt worden."
        <br/><i>Aartsbisschop Desmond Tutu (vrij vertaald)</i>

        <br/><br/>Het Ubuntu-besturingssysteem, gebaseerd op gratis software en vrij van <i>softwarepatenten*</i>,
        brengt het idee van 'ubuntu' naar de wereld van de software.</p>

    <p>* = Een softwarepatent is een octrooi (patent) op een techniek die gebruikt kan worden voor een
        computerprogramma.</p>

