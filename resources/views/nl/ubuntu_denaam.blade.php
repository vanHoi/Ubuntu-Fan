    <h6>Versienummers</h6>
    <p>De versienummers van Ubuntu zijn gebaseerd op de datum waarop die versie uitkomt. Het bestaat uit het jaar en de
        maand van uitgifte en het zegt dus niets over de werkelijke versie van de software.

        <br/><br/>Onze eerste uitgave (Warty Warthog) was in oktober 2004, dus zijn versienummer is 4.10. Versie 12.04
        (Precise Pangolin) is dus uitgebrcht in april 2012!

        <br/><br/>Ook in de codenamen zit een bepaalde structuur. Men gaat namelijk het alfabet af. Ook bestaat de
        codenaam altijd uit twee delen. Het eerste deel is een eigenschap (Precise) en het tweede deel is een dier
        (Pangolin). In het Nederlands vertaald heet versie 12.04 dus Precies Schubbendier. Wat mij betreft klinkt het
        Engels toch beter.</p>

