<h1>Voor- & Nadelen van Ubuntu</h1>

<p>Hier kunt u enkele voor- en nadelen van Ubuntu tegenover Windows vinden.</p>

<h3>Voordelen</h3>
<ul>
    <li>Ubuntu is veilig; je hebt (bijna) geen last van virussen, spyware en trojans.</li>
    <li>Ubuntu is stabiel; je PC wordt na verloop van tijd niet trager en kan jaren draaien op één installatie.</li>
    <li>Ubuntu is gratis; je beschikt over een legaal besturingsysteem voor niets.</li>
    <li>Ubuntu stelt geen hoge eisen aan de hardware van een PC.</li>
    <li>Ubuntu is gebruiksvriendelijk; iedereen kan het gebruiken.</li>
    <li>Ubuntu herkent heel veel hardware; drivers installeren is (bijna) nooit nodig.</li>
    <li>Ubuntu is snel; het start snel op, het heeft geen vertragende virusbeveiliging nodig</li>
    <li>Ubuntu beschikt over heel veel gratis programma's; Negen op de tien keer dat u denkt: 'Zou hier maar een gratis
        programma voor zijn', bestaat dat programma ook. Zit het niet in de pakketbeheren, google dan eens!
    </li>
    <li>Ubuntu heeft een centraal update systeem, dus niet ieder programma vraagt iedere keer te updaten, dit wordt
        centraal geregeld!
    </li>
    <li>Als u een probleem heeft staat een grote online community klaar om u te <a href="/forum">helpen</a>!
    </li>
    <li>Voor de een belangrijker als voor de ander, maar u maakt gebruik van een stukje software wat relatief niet veel
        mensen gebruiken.
    </li>
</ul>

<h3>Nadelen</h3>
<ul>
    <li>Ubuntu kan standaard niet met .exe overweg; dat wil zeggen dat u met Ubuntu standaard geen Windows programma's
        kunt gebruiken.
    </li>
    <li>Als uw driver niet standaard is meegeleverd, oftewel iets standaard niet werkt op uw computer, kan het
        installeren ervan een moeilijke klus zijn.
    </li>
    <li>Voor relatief veel dingen heeft u nog de opdrachtregel (beter bekend als de terminal) nodig. Steeds meer dingen
        kunnen echter ook via de 'normale' weg, alleen dit is vaak iets omslachtiger.
    </li>
    <li>Voor vragen e.d. kunt u minder snel bij vrienden terecht.</li>
</ul>

