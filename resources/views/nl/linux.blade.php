    <p>Elk besturingssysteem heeft voordelen en nadelen. Linux is erg stabiel en flexibel, en ook behoorlijk
        gebruikersvriendelijk, hoewel er hier en daar nog wel eens een steekje los zit. Maar is dat bij Windows dan
        niet. Nee zult u waarschijnlijk denken. Maar dat komt omdat u niet beter weet dan Windows te gebruiken.</p>

