    <p>Eigenlijk is het hetzelfde, alleen sommige mensen noemen Linux liever zo. Waarom zult u u afvragen, dat komt
        omdat Linux onderdelen van het GNU-project gebruikt. En omdat veel Linuxdistributies belangrijke onderdelen uit
        GNU halen, geven sommige gebruikers en ontwikkelaars de voorkeur aan de naam GNU/Linux.</p>

    <p>Kort samengevat is het GNU project gestart om een vrij besturingssysteem te maken. Het is in 1984 gestart door
