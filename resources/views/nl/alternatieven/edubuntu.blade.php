    <p>
        Het primaire doel van Edubuntu is het mogelijk maken dat een docent of ouder met beperkte technische kennis in
        staat is om een computerlab of een online leeromgeving in minder dan een uur op te zetten, en om die omgeving
        eenvoudig te kunnen beheren. Deze distributie is gemaakt met het oog op educatie en toezicht; de docent of
        ouder kan zelf bepalen welke websites bezocht mogen worden en welke programma’s de kinderen mogen openen.
    </p>
    <p>
        Edubuntu is geschikt voor gebruikers van 6 tot 18 jaar en bevat een groot aantal educatieve programma’s. Voor de
        ouder of docent is er een centraal beheer van configuratie, gebruikers en processen opgezet. Ook zijn er
        samenwerkmogelijkheden in een klassikale omgeving en ondersteunt Edubuntu een LTSP-thin-client-architectuur die
        het mogelijk maakt om gebruik te maken van relatief goedkope en energiezuinige gebruikerscomputers die worden
        aangesloten op een centrale server.
