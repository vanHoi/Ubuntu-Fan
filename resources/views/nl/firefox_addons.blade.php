    <h6>uBlock Origin</h6>
    <p>Advertenties en pop-ups ook zo zat? Installeer deze extensies en ze zijn verleden tijd. Vergeet echter niet dat
        veel
        sites geld verdienen via advertenties, dus schakel hem uit op site's die u vaak bezoekt of bevallen!</p>

    <h6>I Don't Care About Cookies</h6>
    <p>Ook al zat van al die cookiemeldingen overal? Installeer deze extensie en de meldingen zijn verleden tijd!</p>

    <h6>SearchPreview</h6>
    <p>Zet voor alle zoekopdrachtenm via Google en Yahoo! een kleine thumbnail van de website.</p>

    <h6>LastPass</h6>
    <p>Wachtwoordmanager. Onthoud en versleuteld veilig al uw wachtwoorden op meerdere apparaten. Eventueel kunt u
        betalen
        voor meer functies.</p>

    <h6>Stylish</h6>
    <p>Hiermee kunt u websites er anders uit laten zien. Installeer de add-on, en kijk vervolgens op de <a
                href="https://userstyles.org/" target="_blank">site van Stylish</a> voor een thema. Voeg deze toe en er
        is
        direct resultaat!</p>

    <h6>Session Manager</h6>
    <p>Slaat alle tabbladen en vensters die u open heeft staan regelmatig op, mocht Firefox een keer crashen. Daarnaast
        kunt
        u ze ook handmatig opslaan om later makkelijk naar terug te keren.</p>

