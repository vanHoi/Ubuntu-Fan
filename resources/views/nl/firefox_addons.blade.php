    <p>Extensies, of add-ons zijn in feite kleine programmaatjes die meer functionaliteit aan Firefox kunnen toevoegen,
        hieronder heb ik mijn favorieten gezet, u kunt <a href="https://addons.mozilla.org/nl/firefox/themes/"
