    <p>U kunt ook klikken op de titel van de extensie om direct naar de Firefox Add-Ons pagina te gaan!</p>

    <h6><a href="https://addons.mozilla.org/nl/firefox/addon/ublock-origin/" target="_blank">
            uBlock Origin</a></h6>
    <p>Advertenties en pop-ups ook zo zat? Installeer deze extensies en ze zijn verleden tijd. Vergeet echter niet dat
        veel
        sites geld verdienen via advertenties, dus schakel hem uit op site's die u vaak bezoekt of bevallen!</p>

    <h6><a href="https://addons.mozilla.org/nl/firefox/addon/i-dont-care-about-cookies/">
            I Don't Care About Cookies</a></h6>
    <p>Ook al zat van al die cookiemeldingen overal? Installeer deze extensie en de meldingen zijn verleden tijd!</p>

    <h6><a href="https://addons.mozilla.org/nl/firefox/addon/searchpreview/" target="_blank">
            SearchPreview</a></h6>
    <p>Zet voor alle zoekopdrachtenm via Google en Yahoo! een kleine thumbnail van de website.</p>

    <h6><a href="https://addons.mozilla.org/nl/firefox/addon/bitwarden-password-manager/" target="_blank">
            Bitwarden</a></h6>
    <p>Bitwarden is een wachtwoordmanager. Het maakt het makkelijk om moeilijke wachtwoorden te gebruiken, zonder dat u
        ze allemaal vergeet. Bitwarden bied alle belangrijke functies aan in de gratis versie, en het is ook nog eens
        open source!
    </p>

    <h6><a href="https://addons.mozilla.org/nl/firefox/addon/forecastfox-fix-version/" target="_blank">
            Forecastfox (fix version)</a></h6>
    <p>Geeft het weer weer in uw browser. Met kaart en de mogelijkheid meerdere dagen vooruit te kunnen kijken!</p>
